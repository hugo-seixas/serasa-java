package tiposprimitivos;

public class TiposPrimitivos {
    public static void main(String[] args) {
        char sexo = 'F';
        byte idade = 45;
        short codigo = 32767;
        int aluno = 100;
        int classes = 10;
        long habitantes = 950554215;
        float nota = 9.5f;
        double dolar = 3.45;
        boolean alternativa = true;
        
        System.out.println("Sexo: " + sexo + " Idade: " + idade + " Código: " + codigo);
        System.out.println("Alunos: " + aluno + " Classes: " + classes + " Habitantes: " + habitantes);
        System.out.println("Nota: " + nota + " Dólar: " + dolar + " Alternativa: " + alternativa);

    }

}